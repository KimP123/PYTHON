def add(a,b):
    return a+b
def sub(a,b):
    return a-b
def div(a,b):
    return a/b
def mult(a,b):
    return a*b

num1=int(input("Enter the First Number Here "))
num2=int(input("Enter the Second Number Here "))
print("The sum of the Numbers is ", add(num1,num2))
print("The difference of the Numbers is ", sub(num1,num2))
print("The quotient of the Numbers is ", div(num1,num2))
print("The product of the Numbers is ", mult(num1,num2))